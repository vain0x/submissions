const P: i64 = 1_000_000_007;

pub fn pow(x: i64, n: i64) -> i64 {
    let (mut x, mut y, mut n) = (x % P, 1_i64, n);
    while n > 0 {
        if n % 2 != 0 {
            y = (y * x) % P;
            n -= 1;
        }

        x = (x * x) % P;
        n /= 2;
    }
    y
}

fn main() {
    let (_, K) = read!(usize, i64);
    let A = read![[i64]];
    let N = A.len();

    let mut u = 0_i64;
    let mut pair = 0_i64;
    for i in 0..N {
        for j in i + 1..N {
            if A[i] > A[j] {
                u += 1;
            }
        }

        for j in 0..N {
            if A[j] > A[i] {
                pair += 1;
            }
        }
    }
    debug!(u, pair, K * (K - 1) / 2);

    let v = (K * (K - 1) / 2) % P * pair % P;

    println!("{}", (u * K % P + v) % P)
}
