fn dfs(n: i64, b3: bool, b5: bool, b7: bool, m: i64) -> i64 {
    if n > m {
        return 0;
    }

    let mut k = 0_i64;

    if b3 && b5 && b7 {
        k += 1;
    }

    k += dfs(n * 10 + 3, true, b5, b7, m);
    k += dfs(n * 10 + 5, b3, true, b7, m);
    k += dfs(n * 10 + 7, b3, b5, true, m);

    k
}

fn main() {
    let m = read!(i64);
    println!("{}", dfs(0, false, false, false, m));
}
