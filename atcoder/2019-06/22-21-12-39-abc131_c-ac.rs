pub fn gcd(x: i64, y: i64) -> i64 {
    if y == 0 {
        x.abs()
    } else {
        gcd(y, x % y)
    }
}

fn anti_division_count(X: i64, C: i64, D: i64) -> i64 {
    let mut n = 0;
    n += X / C;
    n += X / D;
    n -= X / (C / gcd(C, D) * D);

    X - n
}

fn main() {
    let (A, B, C, D) = read!(i64, i64, i64, i64);

    let b = anti_division_count(B, C, D);
    let a = anti_division_count(A - 1, C, D);
    debug!(b, a);

    println!("{}", b - a)
}
