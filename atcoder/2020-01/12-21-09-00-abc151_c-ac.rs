/*
2 5
1 WA
1 WA
2 WA
2 AC
2 WA
=>
1 1
*/

fn main() {
    let (N, M) = read!(usize, usize);

    let mut ac = vec![false; N];
    let mut penalty = vec![0; N];

    for (mut p, s) in read![usize, String; M] {
        p -= 1;

        if ac[p] {
            continue;
        }

        match s.as_str() {
            "AC" => {
                ac[p] = true;
            }
            "WA" => {
                penalty[p] += 1;
            }
            _ => unreachable!("invalid input"),
        }
    }

    let mut s = 0;
    let mut t = 0;
    for i in 0..N {
        if ac[i] {
            s += 1;
            t += penalty[i];
        }
    }

    println!("{} {}", s, t)
}
