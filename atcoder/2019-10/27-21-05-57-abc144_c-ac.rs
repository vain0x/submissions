fn divisors(x: i64) -> Vec<i64> {
    let mut v = vec![];

    let mut d = 1;
    while d * d <= x {
        if x % d == 0 {
            v.push(d);
            v.push(x / d);
        }
        d += 1;
    }

    v.sort();
    v.dedup();

    v
}

fn distance(x: i64, y: i64) -> i64 {
    (x - 1) + (y - 1)
}

fn main() {
    let N = read!(i64);

    let mut m = std::i64::MAX;
    let ds = divisors(N);
    for d in ds {
        m = min(m, distance(d, N / d));
    }

    println!("{}", m)
}
